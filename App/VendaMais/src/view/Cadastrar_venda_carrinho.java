    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) tabela_produtos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO produto = new ProdutoDAO();

        produto.getList().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getNome()
            });
        });
    }

